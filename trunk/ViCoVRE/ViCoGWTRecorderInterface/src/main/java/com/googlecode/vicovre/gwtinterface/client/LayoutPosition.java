package com.googlecode.vicovre.gwtinterface.client;

public class LayoutPosition {

    private String name = null;

    private int x = 0;

    private int y = 0;

    private int width = 0;

    private int height = 0;

    private boolean assignable = false;

    public LayoutPosition(String name, int x, int y, int width, int height,
            boolean assignable) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.assignable = assignable;
    }

    public String getName() {
        return name;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public boolean isAssignable() {
        return assignable;
    }
}
